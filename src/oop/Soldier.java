package oop;

/**
 * Клас {@code Soldier} представляє солдата — базового військовослужбовця
 * без додаткових атрибутів, окрім тих, що визначені у {@link Serviceman}.
 *
 * <p>Клас реалізує поведінку солдата: відображення інформації та базовий
 * розрахунок зарплати. У порівнянні з офіцерами, солдат має нижчий
 * коефіцієнт нарахування.</p>
 */
public class Soldier extends Serviceman {

    /**
     * Конструктор за замовчуванням.
     * Створює солдата з типовими значеннями:
     * імʼя — «Безіменний солдат», звання — «солдат», стаж — 0 років.
     */
    public Soldier() {
        super("Безіменний солдат", "солдат", 0);
    }

    /**
     * Створює солдата з конкретними даними.
     *
     * @param name           ПІБ військовослужбовця
     * @param rank           військове звання
     * @param yearsOfService стаж служби у роках
     */
    public Soldier(String name, String rank, int yearsOfService) {
        super(name, rank, yearsOfService);
    }

    /**
     * Виводить інформацію про солдата у стандартному форматі:
     * <pre>
     * Солдат: <імʼя>, звання: <звання>, років служби: <стаж>
     * </pre>
     *
     * <p>Перевизначає метод {@link Serviceman#showInfo()}.</p>
     */
    @Override
    public void showInfo() {
        System.out.println("Солдат: " + getName() +
                ", звання: " + getRank() +
                ", років служби: " + getYearsOfService());
    }

    /**
     * Обчислює зарплату солдата.
     * Базовий оклад становить 15000 грн, а за кожен рік служби додається 400 грн.
     *
     * @return обчислена зарплата
     */
    @Override
    public double calculateSalary() {
        return 15000 + getYearsOfService() * 400;
    }

    /**
     * Повертає рядок, що описує виконання солдатом його службового обовʼязку.
     *
     * @return текст виду: «Солдат &lt;ПІБ&gt; несе службу на посту.»
     */
    public String performDuty() {
        return "Солдат " + getName() + " несе службу на посту.";
    }
}
